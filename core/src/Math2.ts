export class Math2 {
    public static sum(num1: number, num2: number): number {
        return num1 + num2;
    }

    public static minus(num1: number, num2: number): number {
        return num1 - num2;
    }

    public static multiply(num1: number, num2: number): number {
        return num1 * num2;
    }
}